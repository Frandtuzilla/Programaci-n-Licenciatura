let numero1 = 10;
let numero2 = 20;

// Comparar si el primer número es mayor que el segundo
if (numero1 > numero2) {
    console.log("El primer número (" + numero1 + ") es mayor que el segundo número (" + numero2 + ").");
}
// Comparar si los dos números son iguales
else if (numero1 === numero2) {
    console.log("El primer número (" + numero1 + ") es igual al segundo número (" + numero2 + ").");
}
// Si ninguna de las anteriores es verdadera, entonces el segundo número es mayor
else {
    console.log("El segundo número (" + numero2 + ") es mayor que el primer número (" + numero1 + ").");
}

